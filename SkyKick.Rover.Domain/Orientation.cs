namespace SkyKick.Rover.Domain
{
    /// <summary>
    /// Enumeration of valid orientations, which currently includes just the 4 cardinal directions: North (N), East (E), South (S), and West (W)
    /// </summary>
    public enum Orientation
    {
        Unknown = 0,
        N = 1,
        E = 2,
        S = 3,
        W = 4
    }
}